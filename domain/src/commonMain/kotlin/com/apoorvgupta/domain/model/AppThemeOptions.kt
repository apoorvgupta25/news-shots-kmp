package com.apoorvgupta.domain.model

/**
 * Enum to represent the app theme selected by the user.
 *
 * @author Apoorv Gupta
 */
enum class AppThemeOptions {

    /**
     * Light app theme.
     */
    LIGHT,

    /**
     * Dark app theme.
     */
    DARK,

    /**
     * System-based app theme.
     */
    SYSTEM,
}
